public class L05Loops {
    public static void main(String[] args) {
        // Prints the numbers from 1 to 10
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        } // Iterates through the loop as long as i is less than or equal to 10. i is incremented every loop.

        int i = 1;
        while (i <= 10) {
            System.out.println(i);
            i++;
        } // Does the same thing
    }
}
