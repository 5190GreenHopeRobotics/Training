public class E03HelloWorldManyTimes {
    public static void main(String[] args) {
        // 10 Times
        for (int i = 0; i < 10; i++) {
            System.out.println("Hello World");
        }

        // 1000 Times
        for (int i = 0; i < 1000; i++) {
            System.out.println("Hello World");
        }
    }
}
